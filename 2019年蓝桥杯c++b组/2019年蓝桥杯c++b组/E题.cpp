class node {
public:
	int x;
	int y;
	int re;
	int go;
	int deep;
};
int next_[4][2] = {
	{1, 0}, //下
	{0, -1}, // 左
	{0, 1}, // 右
	{-1, 0} // 上
};

void bfs();


int maine() {
	clock_t startTime, endTime;
	startTime = clock();
	bfs();
	endTime = clock();
	cout << "Totle Time : " << (double)(endTime - startTime) / CLOCKS_PER_SEC << "s" << endl;
	getchar();
	return 0;
}

void bfs() {
	queue<node>q;
	bool visit[n][m] = { false };	// 灰色
	node vs, vd, vn, vw;
	vs.x = 0;
	vs.y = 0;
	vs.deep = 0;
	vd.x = 29;
	vd.y = 49;
	q.push(vs);
	visit[vs.x][vs.y] = true;
	while (!q.empty()) {
		vn = q.front();
		q.pop();
		for (int i = 0; i < 4; i++) {
			vw.x = vn.x + next_[i][0];
			vw.y = vn.y + next_[i][1];
			vw.deep = vn.deep + 1;
			if (vw.x >= 30 || vw.y >= 50 || vw.x < 0 || vw.y < 0)
				continue;
			if ((vw.x == vd.x) && (vw.y == vd.y)) {// 是否为终点
				cout << "找到了" << vw.deep << endl;
				return;
			}
			if ((visit[vw.x][vw.y] == false) && (map[vw.x][vw.y] == 0)) {// 是否符合条件
				q.push(vw);
				visit[vw.x][vw.y] = true;
			}
		}
	}
	cout << "没找到" << endl;
}
