	int status = 0;
	for (int i = 0; i <= 9; i++)
		for (int j = 0; j <= 9; j++)
			for (int k = 0; k <= 9; k++)
				for (int l = 0; l <= 9; l++)
					for (int m = 0; m <= 9; m++)
						for (int n = 0; n <= 9; n++) {
							int sum = i * 100000 + j * 10000 + k * 1000 + l * 100 + m * 10 + n;
							int sum2 = i + j + k + l + m + n;
							if (sum2 == a) {
								if (sum > 10000 && sum <= 99999) {
									if (j == n && k == m) {
										status = 1;
										cout << sum << endl;
									}
								}
								else if (sum > 100000 && sum <= 999999)
									if (i == n && j == m && k == l) {
										status = 1;
										cout << sum << endl;
									}
							}
						}
	if (!status)
		cout << -1 << endl;
